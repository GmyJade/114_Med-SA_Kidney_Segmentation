import os
import json
import numpy as np
import torch
from torch.utils.data import Dataset
import nibabel as nib
import cv2  # 需要安裝: pip install opencv-python
from utils import random_click

class KITS(Dataset):
    def __init__(self, args, data_path, transform=None, transform_msk=None, mode='Training', prompt='click'):
        self.args = args
        self.data_path = data_path
        self.mode = mode
        self.prompt = prompt
        self.img_size = args.image_size
        self.out_size = args.out_size
        self.full_slice_eval = bool(getattr(args, 'full_slice_eval', False))
        self.plane = getattr(args, 'plane', None) or getattr(args, 'slice_plane', 'coronal')
        if self.plane not in ['axial', 'coronal', 'sagittal']:
            raise ValueError(f"Unsupported plane: {self.plane}")
        
        # Read the KiTS-style dataset.json generated by the preprocessing step.
        json_path = os.path.join(data_path, "dataset.json")
        with open(json_path, 'r') as f:
            data_info = json.load(f)
            
        # 根據 mode 選擇對應的列表
        key = 'training' if mode == 'Training' else 'validation'
        self.data_list = data_info[key]
        self.slice_records = None

        if mode != 'Training' and self.full_slice_eval:
            self.slice_records = []
            plane_axis = self._slice_axis()
            for case_index, item in enumerate(self.data_list):
                img_rel_path = item['image'].replace('./', '')
                lbl_rel_path = item['label'].replace('./', '')
                img_path = os.path.join(self.data_path, img_rel_path)
                mask_path = os.path.join(self.data_path, lbl_rel_path)
                if not os.path.exists(img_path):
                    raise FileNotFoundError(f"KiTS {mode} image not found: {img_path}")
                if not os.path.exists(mask_path):
                    raise FileNotFoundError(f"KiTS {mode} label not found: {mask_path}")
                num_slices = nib.load(img_path).shape[plane_axis]
                for slice_idx in range(num_slices):
                    self.slice_records.append({
                        'case_index': case_index,
                        'slice_idx': slice_idx,
                    })

        if self.slice_records is None:
            print(f"[{mode}] Loaded {len(self.data_list)} samples from {json_path} ({self.plane} plane)")
        else:
            print(f"[{mode}] Loaded {len(self.data_list)} cases / {len(self.slice_records)} {self.plane} slices from {json_path}")

    def __len__(self):
        if self.slice_records is not None:
            return len(self.slice_records)
        return len(self.data_list)

    def _preprocess_ct(self, img_slice):
        """
        CT 影像預處理: Windowing + Normalization
        Target: Kidney/Tumor/Abdomen
        Window Level: 50, Window Width: 400 (範圍約 -150 ~ 250)
        或是使用較寬的範圍: -200 ~ 400
        """
        min_bound = -200.0
        max_bound = 400.0
        
        # Clip 數值
        img_slice = np.clip(img_slice, min_bound, max_bound)
        
        # Normalize 到 [0, 1]
        img_slice = (img_slice - min_bound) / (max_bound - min_bound)
        
        return img_slice

    def _slice_axis(self):
        # Match the Axial training repo's KiTS23 plane mapping.
        return {'axial': 0, 'coronal': 1, 'sagittal': 2}[self.plane]

    def _take_slice(self, volume, slice_idx):
        axis = self._slice_axis()
        if axis == 0:
            return volume[slice_idx, :, :]
        if axis == 1:
            return volume[:, slice_idx, :]
        return volume[:, :, slice_idx]

    def _foreground_slices(self, mask_vol):
        axis = self._slice_axis()
        sum_axes = tuple(i for i in range(mask_vol.ndim) if i != axis)
        return np.where(np.sum(mask_vol, axis=sum_axes) > 0)[0]

    def __getitem__(self, index):
        # 1. 取得檔案路徑 (修正相對路徑問題)
        fixed_slice_idx = None
        if self.slice_records is None:
            item = self.data_list[index]
        else:
            record = self.slice_records[index]
            item = self.data_list[record['case_index']]
            fixed_slice_idx = record['slice_idx']
        # dataset.json 裡的像 "./imagesTr/xxx.nii.gz"，需要去掉開頭的 ./
        img_rel_path = item['image'].replace('./', '')
        lbl_rel_path = item['label'].replace('./', '')
        
        img_path = os.path.join(self.data_path, img_rel_path)
        mask_path = os.path.join(self.data_path, lbl_rel_path)
        if not os.path.exists(img_path):
            raise FileNotFoundError(f"KiTS image not found: {img_path}")
        if not os.path.exists(mask_path):
            raise FileNotFoundError(f"KiTS label not found: {mask_path}")

        # 2. 讀取 3D NIfTI
        # 載入後的 shape 通常是 (H, W, D)
        img_nii = nib.load(img_path)
        img_vol = img_nii.get_fdata()
        mask_vol = nib.load(mask_path).get_fdata()

        # 3. 3D -> 2D 切片選擇策略
        # 我們不能一次把整個 3D 丟進去，必須選一張 Slice
        slice_axis = self._slice_axis()
        num_slices = mask_vol.shape[slice_axis]
        voxel_spacing = img_nii.header.get_zooms()[:3]
        slice_axes = tuple(i for i in range(mask_vol.ndim) if i != slice_axis)
        slice_h, slice_w = (mask_vol.shape[axis] for axis in slice_axes)
        original_pixel_area_mm2 = float(voxel_spacing[slice_axes[0]] * voxel_spacing[slice_axes[1]])
        resized_pixel_area_mm2 = float(
            (slice_h * voxel_spacing[slice_axes[0]]) *
            (slice_w * voxel_spacing[slice_axes[1]]) /
            (self.out_size * self.out_size)
        )
        
        if fixed_slice_idx is None:
            foreground_slices = self._foreground_slices(mask_vol)

            if self.mode == 'Training':
                # Training samples one 2D slice per case, biased toward foreground.
                if len(foreground_slices) > 0:
                    if np.random.rand() < 0.8:
                        slice_idx = np.random.choice(foreground_slices)
                    else:
                        slice_idx = np.random.randint(0, num_slices)
                else:
                    slice_idx = np.random.randint(0, num_slices)
            else:
                # Lightweight validation during training: match the coronal repo by
                # using the middle foreground slice instead of all slices.
                if len(foreground_slices) > 0:
                    slice_idx = int(foreground_slices[len(foreground_slices) // 2])
                else:
                    slice_idx = num_slices // 2
        else:
            slice_idx = fixed_slice_idx

        # 取出 2D Slice
        img = self._take_slice(img_vol, slice_idx)
        mask = self._take_slice(mask_vol, slice_idx)

        # 4. CT 數值預處理 (Windowing)
        img = self._preprocess_ct(img)

        # 5. Resize (使用 cv2 進行正確的插值)
        img = cv2.resize(img, (self.img_size, self.img_size), interpolation=cv2.INTER_LINEAR)
        mask = cv2.resize(mask, (self.out_size, self.out_size), interpolation=cv2.INTER_NEAREST)

        # 6. 轉為 Tensor 並處理通道
        img = torch.tensor(img).float().unsqueeze(0) 
        img = img.repeat(3, 1, 1) # [3, 1024, 1024]

        mask = torch.tensor(mask).unsqueeze(0).float() # [1, out_size, out_size]

        # 7. 生成 Prompt (Click)
        pt = None
        point_label = None
        
        if self.prompt == 'click':
            binary_mask = (mask[0] > 0).int().numpy()
            point_label, pt = random_click(binary_mask, 1)

        case_id = os.path.basename(img_rel_path).replace('.nii.gz', '')
        image_meta_dict = {
            'filename_or_obj': f"{item['image']}_{self.plane}_slice{slice_idx}",
            'case_id': case_id,
            'slice_idx': int(slice_idx),
            'num_slices': int(num_slices),
            'image_path': img_path,
            'label_path': mask_path,
            'plane': self.plane,
            'slice_axis': int(slice_axis),
            'voxel_spacing_axis0_mm': float(voxel_spacing[0]),
            'voxel_spacing_axis1_mm': float(voxel_spacing[1]),
            'voxel_spacing_axis2_mm': float(voxel_spacing[2]),
            'original_slice_height': int(slice_h),
            'original_slice_width': int(slice_w),
            'original_pixel_area_mm2': original_pixel_area_mm2,
            'resized_pixel_area_mm2': resized_pixel_area_mm2,
        }
        
        # print("dataset image shape =", img.shape)
        # print("dataset label shape =", mask.shape)
        
        return {
            'image': img,
            'label': mask,
            'p_label': point_label,
            'pt': pt,
            'image_meta_dict': image_meta_dict,
        }


